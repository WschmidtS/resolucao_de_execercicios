'''
Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
'''
print('Cálcumo da área de um quadrado imprimindo o dobro dessa área')
lado_quadrado = float(input('Tamanho do lado do Quadrado em cm:'))
resultado = (lado_quadrado**2) * 2
print(f'O dobro da área do quadrado é: {resultado} cm²')
